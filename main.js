
var toggleDocumentation = (function() {
    var visible = false;
    var div = document.getElementById('documentation');
    return function () {
        if (visible) {
            div.style = 'display: none';
        } else {
            div.style = 'display: block';
        }
        visible = !visible;
    };
})();

